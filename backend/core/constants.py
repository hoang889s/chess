PIECE_TYPES = ('P', 'N', 'B', 'R', 'Q', 'K')
PIECE_SCORE_PIECES = tuple(
    piece for piece_type in PIECE_TYPES for piece in (piece_type, piece_type.lower())
)

PIECE_SCORES = {}
for _piece in PIECE_SCORE_PIECES:
    _piece_type = _piece.upper()
    _table = PST[_piece_type]
    _value = PIECE_VALUES[_piece_type]
    PIECE_SCORES[_piece] = [[0 for _ in range(8)] for _ in range(8)]
    for _r in range(8):
        for _c in range(8):
            _bonus = _table[_r][_c] if _piece.isupper() else _table[7 - _r][_c]
            PIECE_SCORES[_piece][_r][_c] = _value + _bonus if _piece.isupper() else -(_value + _bonus)

